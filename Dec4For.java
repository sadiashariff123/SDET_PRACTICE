
public class Dec4For {

	public static void main(String[] args) {
		//for (statement 1; statement 2; statement 3) 
		
		for (int i = 0; i <= 10; i = i + 2) {
			  System.out.println(i);
		
		}
		String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
		for (String i : cars) {
		  System.out.println(i);
		}
	}

}
