
public class Dec3multidimention {

	public static void main(String[] args) {
		int[][] numbers =  { {2,5,78},
				  			{4,90,45},
				  			{67,45,23}
				  };
		// {rows},{column}
		//ForLoop
		
		
	  for(int i=0; i<3; i++) {
			for(int j=0; j<3; j++) {
			System.out.println(numbers [i][j]);
			}
			
		}
		
	}
	
}
	
